let first_input = null;
let second_input = null;

function add(first_input, second_input) {
    return first_input + second_input;
}
function subtract(first_input, second_input) {
    return first_input - second_input;
}
function multiply(first_input, second_input) {
    return first_input * second_input;
}
function divide(first_input, second_input) {
    return first_input / second_input;
}
